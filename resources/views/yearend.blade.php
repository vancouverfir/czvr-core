    <h1 class="font-weight-bold blue-text">A Letter from Nate</h1>
    <br>
        <p>Friends,</p>
            <p>As we look back on 2021 – a year that, like 2020, was truly unique in many ways – I once again have the chance to say a few words to 
            the controllers, staff, and pilots here in Winnipeg.</p>
        <hr>
        <h5 class="blue-text font-weight-bold">To my Staff,</h5>
        <br>
            <p>A year ago, my yearly letter talked about how I was looking back on finally having a fully staffed Winnipeg FIR – and since 2020, 
            the staff of Winnipeg has certainly changed a bit, as do all FIRs on the network. And while Kolby and I remain in our positions as Deputy 
            Chief and FIR Chief respectively, the rest of our FIR’s staff has seen some changes, both big and small this past year.</p>
            
            <p>Our Chief Instructor position saw a few familiar names – from Dario Marnika, and now Luke Wightman – who is back on the FIR’s staff 
            team for the second time. He has some large ambitions for our instructing team in the new year – and we’re all excited to see what’s in store.</p>
            
            <p>The position of Events Coordinator is often a position on the network with more turnover – and Winnipeg saw a new name take on the role
            in 2021. Tavis Harrison, one of Winnipeg’s students who’s been with the FIR from S1 to C1, took on the job and has since already planned a 
            flurry of successful events – with more on the way in 2022.</p>
            
            <p>We also saw Winnipeg get a new Facility Engineer in Ryan Miltenberger – who came from the Toronto FIR to join the team and help modernize 
            the FIR’s sector files – now, our scopes are among the most modern in VATCAN, and more work continues to make them even better.</p>
            
            <p>Our Webmaster position saw a bit of a different change this year – James White didn’t leave his position – but he did learn he’s 
            becoming something to much more important in the new year - a father. We often forget about the real world when we’re on this network 
            so often – so hearing news like this always brings a smile to my face. On the VATSIM side of things, James continues working on tweaks 
            to the Winnipeg FIR website and on a few other secret projects as well. Stay tuned for more on those.</p>
            
            <p>Since I started hiring staff for the FIR, I’ve always taken pride in the incredible teamwork we have here. I’m happy to see that our 
            team continues to stand strong as we look to 2022. I can’t wait for the progress we’ll continue to make in the new year.</p>     
        <hr>
        <h5 class="blue-text font-weight-bold">To the Winnipeg Controllers,</h5>
        <br>
            <p>Controlling in Winnipeg is a bit of a strange situation for myself – as someone who lives in Halifax - about as far away from the FIR in Canada as one
            can get, I often get asked why I've stayed loyal Winnipeg over the years. And with all the time I’ve spent in this FIR, I’ve learned that the question of “Why 
            Winnipeg?” isn’t unique to myself. However, Winnipeg controllers continue to answer the question for me. Winnipeg isn’t just another FIR – we 
            truly are a big family.</p>
            
            <p>From our local events that showed off the amazing prairies, to crossfires that allowed us to work with our neighbouring FIRs and ARTCCs,
            we once again proved in 2021 that Winnipeg is truly one of VATSIM’s greatest places.</p>

            <p>Our training system saw some strains this past year, like many other places. With all the time off many of us shared thanks to the pandemic, 
            training waitlists had to be created – something I would have never expected to see in Winnipeg even just a few years ago. However, our list 
            remains short and moving along. As we move into 2022, we’re confident our training capacity will only continue to increase, and our waitlists 
            will continue to shrink. To those of you who have been waiting or had to wait at some point, thank you for your patience. I promise – the wait 
            in Winnipeg is worth it.</p>
        <hr>
        <h5 class="blue-text font-weight-bold">To the VATCAN Staff,</h5>
        <br>
            <p>I’ve now been here in the Winnipeg FIR Chief’s seat for a couple years (mid-2019 was when I stepped back from VATCAN’s HQ to take on this 
            position, for context.) In the relatively short time I’ve been here, the VATCAN staff has certainly become rather familiar with my name and this FIR.</p>
            
            <p>While not every moment on this network is perfect, and there are always changes to be made and disputes to be resolved, I’m lucky to have 
            had the support of the VATCAN staff throughout my tenure here as Winnipeg Chief, and continue to be extremely grateful for the respect and 
            dignity we are able to share when working together to improve this division we all love.</p>
            
            <p>The VATCAN – Winnipeg relationship continues to be as strong as ever – and I look forward to continue growing that as the calendar flips to 2022.</p>
            <hr>
        <h5 class="blue-text font-weight-bold">On a Personal Note,</h5>
        <br>
            <p>For myself, 2021 both had some changes and some new opportunities. First off – getting back to work at my real-world job was a welcome 
            change after over half a year on layoff status. A year ago, I was talking about how I was hopeful that the tides would turn, and thankfully, 
            they indeed did.</p>
            
            <p>2021 also allowed me to finally do something I hadn’t had the chance to do – cross off the last FIR in Canada I didn’t yet visit – Winnipeg. 
            Flying into the prairies in the middle of November was, in hindsight, not the best idea – and the winter storm that welcomed me taught me that 
            rather quickly. But landing on Runway 31, taxiing in on the taxiways I had become so familiar with on a computer screen, was an experience that 
            still gives me goosebumps just thinking about it. Not to mention, meeting some members of the FIR (shoutout to Liam, Luke, James and Karl) and 
            having an impromptu Real Ops event was a great experience – and I’m sure I’ll be back in the prairies soon. Hopefully not rushed out by a 
            snowstorm, next time.</p>
            
            <p>Speaking of Real Ops, we did it! In September, I hopped on a plane and headed to Toronto – where I met yet another handful of Winnipeg 
            controllers (shoutout Dario, Ryan, Zach, Matt and Ethan) and we hosted the FIR’s first official Real Ops event. It was an amazing experience 
            to be surrounded by Winnipeg controllers – and don’t worry, we had a team in Winnipeg at the same time working with us to fully staff the FIR 
            for the event. Next time, let’s do Real Ops in the FIR’s greatest treasure – Flin Flon.</p>
        
            <p>This past year has been a crazy one – full of changes, but the light at the end of the tunnel is approaching. As we look to 2022, I can’t 
            wait to see what’s in store for the aviation industry and this network. To everyone who flew, controlled, or even liked a few of our tweets 
            this past year, thank you for continuing to help Winnipeg grow. As I approach my 10 year anniversary on the VATSIM network, it is the community 
            that keeps me going. It always has been.</p>
    <hr>
